    private void SignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpActionPerformed
        // TODO add your handling code here:
        SignUp.setEnabled(false);
        boolean usernameChecked = false;
        boolean passwordChecked = false;
        boolean conpasswordChecked = false;
        boolean nameChecked = false;
        boolean phoneChecked = false;
        boolean houseChecked = false;
        boolean streetChecked = false;
        boolean cityChecked = false;
        boolean provinceChecked = false;
        boolean emailChecked = false;
        String validUsername = Validate.validateUsername(username.getText());
        String validPassword[] = Validate.validatePassword(new String(passwordField.getPassword()), new String(passwordFieldConfirm.getPassword()));
        String validName = Validate.validateName(fName.getText(), mName.getText(), lName.getText());
        String validPhone = Validate.validatePhone(phoneNum.getText());
        String validHouse = Validate.validateHouseNum(houseNum.getText());
        String validStreet = Validate.validateStreet(streetAddress.getText());
        String validCity = Validate.validateCity(city.getText());
        String validProvince = Validate.validateProvince(province.getText());
        String validEmail = Validate.validateEmail(email.getText());
        String multiAcc = multiAccount.isSelected() ? "Multi" : "Guest";
        
        if(! validUsername.equals("")) {
            usernameError.setVisible(true);
            usernameError.setText(validUsername);
            usernameChecked = false;
        } else {
            usernameChecked = true;
        }
        
        if(! validPassword[0].equals("")) {
            passwordError.setVisible(true);
            passwordError.setText(validPassword[0]);
            passwordChecked = false;
        }else {
            passwordChecked = true;
        }
        
        if(! validPassword[1].equals("")) {
            confirmPasswordError.setVisible(true);
            confirmPasswordError.setText(validPassword[1]);
            conpasswordChecked = false;
        } else {
            conpasswordChecked = true;
        }

        if(! validName.equals("")) {
            fnameError.setVisible(true);
            mnameError.setVisible(true);
            lnameError.setVisible(true);
            fnameError.setText(validName);
            mnameError.setText(validName);
            lnameError.setText(validName);
            nameChecked = false;
        } else {
            nameChecked = true;
        }
        
        if(! validPhone.equals("")) {
            phoneError.setVisible(true);
            phoneError.setText(validPhone);
            phoneChecked = false;
        } else {
            phoneChecked = true;
        }
        
        if(! validHouse.equals("")) {
            houseNumError.setVisible(true);
            houseNumError.setText(validHouse);
            houseChecked = false;
        } else {
            houseChecked = true;
        }

        if(! validStreet.equals("")) {
            streetError.setVisible(true);
            streetError.setText(validStreet);
            streetChecked = false;
        } else {
            streetChecked = true;
        }

        if(! validCity.equals("")) {
            cityError.setVisible(true);
            cityError.setText(validCity);
            cityChecked = false;
        } else {
            cityChecked = true;
        }

        if(! validProvince.equals("")) {
            provinceError.setVisible(true);
            provinceError.setText(validProvince);
            provinceChecked = false;
        } else {
            provinceChecked = true;
        }
        
        if(! validEmail.equals("")) {
            emailError.setVisible(true);
            emailError.setText(validEmail);
            emailChecked = false;
        } else {
            emailChecked = true;
        }

        if(usernameChecked && passwordChecked && conpasswordChecked && nameChecked && phoneChecked && 
                houseChecked && streetChecked && cityChecked && provinceChecked && emailChecked) {
            Statement st = null;
            boolean executeComplete = false;
            
            try {
                
                st = auth.getConnection().createStatement();
                if(multiAccount.isSelected()) {
                    
                    System.out.println("User Sql Execution...");
                    String user_sql = String.format("INSERT INTO project.users(fname, lname, username, password, phonenum, housenum, street, city, province, email, accounttype, country) "
                            + "VALUES('%s', '%s', '%s', '%s', '%s', %s, '%s', '%s', '%s', '%s', 'Multi', '%s');", fName.getText(), lName.getText(), username.getText(), 
                            new String(passwordField.getPassword()), phoneNum.getText(), houseNum.getText(), streetAddress.getText(), city.getText(), province.getText(), 
                            email.getText(), jComboBox1.getSelectedItem().toString());
                    System.out.println(user_sql);
                    st.execute(user_sql);
                    
                    System.out.println("Host Sql Execution...");
                    String host_sql = String.format("INSERT INTO project.host(fname, lname, email, phonenum, housenum, street, city, province, user_id, country) "
                            + "VALUES ('%s', '%s', '%s', '%s', %s, '%s', '%s', '%s', (SELECT MAX(id) FROM project.users), '%s');", 
                            fName.getText(), lName.getText(), email.getText(), phoneNum.getText(), houseNum.getText(), streetAddress.getText(), 
                            city.getText(), province.getText(), jComboBox1.getSelectedItem().toString());
                    System.out.println(host_sql);
                    st.execute(host_sql);
                    
                    System.out.println("Guest Sql Execution...");
                    String guest_sql = String.format("INSERT INTO project.guest (phonenum, email, fname, lname, housenum, street, city, province, user_id, country) "
                            + "VALUES ('%s', '%s', '%s', '%s', %s, '%s', '%s', '%s', (SELECT MAX(id) FROM project.users), '%s');", phoneNum.getText(), email.getText(), 
                            fName.getText(), lName.getText(), houseNum.getText(), streetAddress.getText(), city.getText(), province.getText(), jComboBox1.getSelectedItem().toString());
                    System.out.println(guest_sql);
                    st.execute(guest_sql);
                    
                    executeComplete = true;
                    st.close();
                } else {
                    
                    System.out.println("User Sql Execution...");
                    String user_sql = String.format("INSERT INTO project.users(fname, lname, username, password, phonenum, housenum, street, city, province, email, accounttype, country) "
                            + "VALUES('%s', '%s', '%s', '%s', '%s', %s, '%s', '%s', '%s', '%s', 'Single', '%s');", fName.getText(), lName.getText(), username.getText(), 
                            new String(passwordField.getPassword()), phoneNum.getText(), houseNum.getText(), streetAddress.getText(), city.getText(), province.getText(), 
                            email.getText(), jComboBox1.getSelectedItem().toString());
                    System.out.println(user_sql);
                    st.execute(user_sql);
                    
                    System.out.println("Guest Sql Execution...");
                    String guest_sql = String.format("INSERT INTO project.guest (phonenum, email, fname, lname, housenum, street, city, province, user_id, country) "
                            + "VALUES ('%s', '%s', '%s', '%s', %s, '%s', '%s', '%s', (SELECT MAX(id) FROM project.users), '%s');", phoneNum.getText(), email.getText(), 
                            fName.getText(), lName.getText(), houseNum.getText(), streetAddress.getText(), city.getText(), province.getText(), 
                            jComboBox1.getSelectedItem().toString());
                    System.out.println(guest_sql);
                    st.execute(guest_sql);
                    
                    st.close();
                    executeComplete = true;
                }
                
            } catch(NullPointerException | SQLException | ClassNotFoundException e) {
                
                Logger.getLogger(SignUpPage.class.getName()).log(Level.SEVERE, e, null);
                executeComplete = false;
                
            } finally {
                
                if (executeComplete) {
                    JOptionPane.showMessageDialog(null, "User Creation Successful", "InfoBox: Success", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                    LoginMain m = new LoginMain(auth);
                    m.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Account Creation Error: Contact Administrator", "InfoBox: Error", JOptionPane.ERROR_MESSAGE);
                    SignUp.setEnabled(true);
                }
                
            }
            // Enable The SignUp button
        } else {
            SignUp.setEnabled(true);
        }
        
    }//GEN-LAST:event_SignUpActionPerformed

