    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        // TODO add your handling code here:
        boolean CleanChecked = false;
        boolean ValueChecked = false;
        boolean OverallChecked = false;
        boolean CommentsChecked = false;
        String validClean = Validate.validateRatings(jComboBox2.getSelectedItem().toString());
        String validValue = Validate.validateRatings(jComboBox4.getSelectedItem().toString());
        String validOverall = Validate.validateRatings(jComboBox1.getSelectedItem().toString());
        String validComm = Validate.validateComm(jTextArea1.getText());
        
        if(! validClean.equals("")) {
            JOptionPane.showMessageDialog(null, "Clean Rating Invalid", "InfoBox: Rating Invalid", JOptionPane.ERROR_MESSAGE);
        } else {
            CleanChecked = true;
        }
        
        if(!validValue.equals("")) {
            JOptionPane.showMessageDialog(null, "Value Rating Invalid", "InfoBox: Rating Invalid", JOptionPane.ERROR_MESSAGE);
        } else {
            ValueChecked = true;
        }
        
        if(!validOverall.equals("")) {
            JOptionPane.showMessageDialog(null, "Overall Rating Invalid", "InfoBox: Rating Invalid", JOptionPane.ERROR_MESSAGE);
        } else {
            OverallChecked = true;
        }
        
        if(!validComm.equals("")) {
            JOptionPane.showMessageDialog(null, "Comment Text Invalid", "InfoBox: Text Invalid", JOptionPane.ERROR_MESSAGE);
        } else {
            CommentsChecked = true;
        }
        
        if(CleanChecked && ValueChecked && OverallChecked && CommentsChecked) {
            boolean completed = false;
            Statement st = null;
            
            try {
                String q = String.format("insert into project.reviews "
                        + "(ratings, cleanliness, rev_value, communication, property_id, host_id, guest_id) values "
                        + "(%s, %s, %s, '%s', %s, (select host_id from project.property where property_id=%s), %s)", 
                        jComboBox1.getSelectedItem().toString(), jComboBox2.getSelectedItem().toString(), jComboBox4.getSelectedItem(), 
                        jTextArea1.getText(), rental.get("property_id"), rental.get("property_id"), rental.get("guest_id"));
                
                st = conn.createStatement();
                st.execute(q);
                st.close();
                completed = true;
            } catch(SQLException ex) {
                Logger.getLogger(Review.class.getName()).log(Level.SEVERE, ex, null);
            }
            
            if (completed) {
                JOptionPane.showMessageDialog(null, "Review Added Successfully", "InfoBox: Review", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Reivew Creation Error", "InfoBox: Review Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_SubmitActionPerformed

