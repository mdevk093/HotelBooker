    private void SignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignActionPerformed
        // TODO add your handling code here:
        boolean startDateChecked    = false;
        boolean endDateChecked      = false;
        boolean paymentChecked      = false;
        String  validateDate         = Validate.validateDate(Date.getText(), StartDate.getText());
        String  validateDateStretch  = Validate.validateDate(StartDate.getText(), EndDate.getText());
        
        if (! validateDate.equals("")) {
            JOptionPane.showMessageDialog(null, "Start Date is before Property AvailableDate", "InfoBox: Invalid Date", JOptionPane.ERROR_MESSAGE);
        } else {
            startDateChecked = true;
        }
        
        if (! validateDateStretch.equals("")) {
            JOptionPane.showMessageDialog(null, "End Date is before Start Date", "InfoBox: Invalid Date", JOptionPane.ERROR_MESSAGE);
        } else {
            endDateChecked = true;
        }
        
        if (jComboBox1.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Select a payment method", "InfoBox: Payment Method Invalid", JOptionPane.ERROR_MESSAGE);
        } else {
            paymentChecked = true;
        }
        
        if (startDateChecked && endDateChecked && paymentChecked) {
            boolean completed  = false;
            Statement st       = null;
            
            try {
                String q = String.format("insert into project.rentalagreement"
                        + "(startdate, enddate, property_id, guest_id) values ('%s', '%s', %s, (select guest_id from project.guest where user_id=%s));", 
                        StartDate.getText(), EndDate.getText(), property.get("property_id"), bundle.get("id"));
                st = conn.createStatement();
                st.execute(q);
                
                q = "select max(rentalagree_id) from project.rentalagreement";
                ResultSet rs = st.executeQuery(q); String rentalagree_id = "";
                while(rs.next()) {
                    rentalagree_id = rs.getString("max");
                }
                rs.close();
                
                q = "select guest_id from project.rentalagreement where rentalagree_id=" + rentalagree_id + ";";
                rs = st.executeQuery(q); String guest_id = "";
                while(rs.next()) {
                    guest_id = rs.getString("guest_id");
                }
                rs.close();
                
                q = "select host_id from project.property where property_id=(select property_id from project.rentalagreement where rentalagree_id=" + rentalagree_id + ");";
                rs = st.executeQuery(q); String host_id = "";
                while(rs.next()) {
                    host_id = rs.getString("host_id");
                }
                rs.close();
                
                q = String.format("insert into project.payment (paymenttype, amount, status, guest_id, host_id, rentalagree_id) "
                        + "values ('%s', %s, 'Pending', %s, %s, %s)", jComboBox1.getSelectedItem().toString(), 1500, guest_id, host_id, rentalagree_id);
                st.execute(q);
                st.close();
                completed = true;
            } catch(SQLException ex) {
                Logger.getLogger(RentalAgreement.class.getName()).log(Level.SEVERE, ex, null);
            }
            
            if(completed) {
                JOptionPane.showMessageDialog(null, "Rental Agreement Contract Created", "InfoBox: Rental Agreement Signed", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Rental Agreement Creation Error", "InfoBox: Rental Agreement Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_SignActionPerformed

